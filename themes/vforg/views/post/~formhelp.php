      <h4>How to Format</h4>
      <ul>
         <li>HTML is allowed</li>
         <li>Place code samples in &lt;pre&gt; and &lt;code&gt; tags.</li>
         <li>Provide details like:
            <ul>
               <li>Exact error messages</li>
               <li>Browser & version</li>
               <li>PHP version</li>
               <li>MySQL version</li>
            </ul>
         </li>
      </ul>
